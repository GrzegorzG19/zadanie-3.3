public class Math {
    boolean isEven(int liczba) {


        return ((liczba % 2) == 0);
    }

    boolean isOdd(int liczba2) {


        return ((liczba2 % 2) != 0);
    }

    double circleField(double liczba3) {
        return (liczba3 * liczba3 * 3.14);
    }

    int potega(int liczba4) {
        return (liczba4 * liczba4);
    }
}
